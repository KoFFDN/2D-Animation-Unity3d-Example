/**
 * Play animation with keboard buttons. 
 * */

using UnityEngine;
using System.Collections;

public class PlayerControl : MonoBehaviour {

	void Update() {
		AnimeScript animation = GetComponent<AnimeScript>();
		// right
		if(Input.GetKey("d")) {
			animation.aniSprite(3,4,0,2,2,10);
		}
		//left
		if(Input.GetKey("a")){
			animation.aniSprite(3,4,0,1,2,10);
		}
		//up
		if(Input.GetKey("w")) {
			animation.aniSprite(3,4,0,3,2,10);	
		}
		//down
		if(Input.GetKey("s")) {
			animation.aniSprite(3,4,0,4,2,10);	
		}
	}
}
